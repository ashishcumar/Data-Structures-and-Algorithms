/*
1.)  Bitwise AND & returns 1 if the corresponding bits of both operands are 1 else it returns 0.
2.)  Bitwise OR | returns 1 if either of the corresponding bits of one operand is 1 else returns 0.
3.)  Bitwise XOR ^ returns 1 if the corresponding bits are different and returns 0 if the corresponding bits are the same.
4.)  Bitwise NOT ~ inverts the bit( 0 becomes 1, 1 becomes 0).
5.)  In the left shift operator <<, the left operand specifies the number and the right operand specifies the number to be shifted left. Zero bits are added to the right and excess bits from the left are discarded.
     Eg:- 1 << 2 
     In Binary 1 is 1 and when we do a left shift of 2 in 1 it will be 100. two zeros added at right side of it and excess bits from the left will be discarded.
6.)  In the right shift operator >>, the first operand specifies the number and the second operand specifies the number to be shifted right. Excess bits from the right are discarded. The copies of the leftmost bit are shifted in from the left, hence the name sign-propagating.
     Eg:-  works same as left shift but direction is towards right



*/
